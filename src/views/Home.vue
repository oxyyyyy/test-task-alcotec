Тестовое задание Front-end
Необходимо создать СПА на Vue.js. СПА должно поддерживать маршрутизацию посредством vuerouter
и иметь хранилище состояния, реализованное через Vuex.
Шапка приложения должна содержать следующие ссылки маршрутов:
1) “Главная” – статическая страница, на которой выводится текст данного ТЗ без картинок.
2) “Вакууматоры” и “Дозаторы” страницы, на которых выводится таблица с товарами. Таблица
содержит следующие столбцы: изображение, наименование, наличие и цена.
Данные для таблицы получаются через API методом POST по адресу
https://dev.alcotec.com.ua/api/products/ID, тело запроса должно содержать следующие
параметры:
sorting: { sortBy: "rating", isDesc: true},
page: 1,
perpage: 15,
filter: {},
Для вакууматоров ID = 322, дозаторов - 103.
Url для доступа к изображениям https://dev.alcotec.com.ua
В api реализовано несколько видов сортировок, НО вам необходимо реализовать сортировку
самостоятельно, на фронте, без запроса к api и использования библиотек сторонних
разработчиков. Сортировка должна быть реализована по параметру “Цена”. При загрузке
сортировка должна быть как в запросе – по рейтингу. По клику на “Цена” – товары должны
сортироваться по ниспадающей цене, при повторном клике – по возрастающей.
3) “Условия возврата” и “Условия оплаты” – страницы, на которых выводится готовый
статический контент с сервера посредством запроса к API методом GET по адресу
https://dev.alcotec.com.ua/api/pages/alias. Для условий возврата alias: purchase-returns, для
оплаты – payment. На страницах необходимо вывести наименование страницы в теге H1 и
контент в отдельном блоке.
